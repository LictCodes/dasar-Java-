public class Variabel {
	public static void main(String[] args) {
		int x,y,z;
		// Ini adalah deklarasi 
		 x = 0;
		// x adalah variabel dengan tipe data integer 
		y = 90;
		z = 70;
		System.out.println("Nilai dari variabel x adalah : " + x);
		System.out.println("Nilai dari variabel y adalah : " + y);
		System.out.println("Nilai dari variabel z adalah : " + z);
		x = 80; 
		System.out.println("\nNilai dari variabel x stelah di ubah adalah : " + x);
		//untuk Menampilkan variabel
	}
}