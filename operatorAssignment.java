public class operatorAssignment {
	public static void main(String[] args) {
		// operator assignment tambah 
		byte a = 10;
		a+= 5 ; // a = a + 5 
		System.out.println(a);
		// operator assignment tambah 
		byte b = 10;
		b-= 5 ; // b = b + 5 
		System.out.println("\n" + b);
		// operator assignment perkalian
		byte  c = 10 ; 
		c *= 5;
		System.out.println("\n" + c);
	}
}